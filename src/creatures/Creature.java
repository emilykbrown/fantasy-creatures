package creatures;

public abstract class Creature {

    private String name;
    private int age;

    public Creature(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Creature [name=" + name + ", age=" + age + "]";
    }
}
